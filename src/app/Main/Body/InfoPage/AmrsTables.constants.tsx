import { AmrsTableData } from "./InfoPage.model";

function createAmrTable(
    substanceClass: string,
    amrSubstance: string,
    years: string[],
    valuesPerYear: string[][]
): AmrsTableData {
    const concentrationList: Record<
        string,
        {
            cutOff: string;
            min: string;
            max: string;
        }
    > = {};

    valuesPerYear.forEach((yearList, index) => {
        concentrationList[years[index]] = {
            cutOff: yearList[0],
            min: yearList[1],
            max: yearList[2],
        };
    });
    return {
        substanceClass,
        amrSubstance,
        concentrationList,
    };
}

