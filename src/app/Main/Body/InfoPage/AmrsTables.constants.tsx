import { AmrsTableData } from "./InfoPage.model";

function createAmrTable(
    substanceClass: string,
    amrSubstance: string,
    valuesPerYear: string[][]
): AmrsTableData {
    const concentrationList: {
        cutOff: string;
        min: string;
        max: string;
    }[] = [];
    valuesPerYear.forEach((yearList) => {
        concentrationList.push({
            cutOff: yearList[0],
            min: yearList[1],
            max: yearList[2],
        });
    });
    return {
        substanceClass,
        amrSubstance,
        concentrationList,
    };
}

